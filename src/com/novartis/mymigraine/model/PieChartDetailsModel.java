package com.novartis.mymigraine.model;

public class PieChartDetailsModel {

	public int count,color;
	public String answer;
	public int refQuestionId;
	public int refAnswerId;
	
	/**
	 * @return the count
	 */
	public int getCount() {
		return count;
	}
	/**
	 * @param count the count to set
	 */
	public void setCount(int count) {
		this.count = count;
	}
	/**
	 * @return the color
	 */
	public int getColor() {
		return color;
	}
	/**
	 * @param color the color to set
	 */
	public void setColor(int color) {
		this.color = color;
	}
	/**
	 * @return the answer
	 */
	public String getAnswer() {
		return answer;
	}
	/**
	 * @param answer the answer to set
	 */
	public void setAnswer(String answer) {
		this.answer = answer;
	}
	/**
	 * @return the refQuestionId
	 */
	public int getRefQuestionId() {
		return refQuestionId;
	}
	/**
	 * @param refQuestionId the refQuestionId to set
	 */
	public void setRefQuestionId(int refQuestionId) {
		this.refQuestionId = refQuestionId;
	}
	/**
	 * @return the refAnswerId
	 */
	public int getRefAnswerId() {
		return refAnswerId;
	}
	/**
	 * @param refAnswerId the refAnswerId to set
	 */
	public void setRefAnswerId(int refAnswerId) {
		this.refAnswerId = refAnswerId;
	}
	
	
}
