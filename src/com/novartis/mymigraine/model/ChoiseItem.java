package com.novartis.mymigraine.model;

import java.io.Serializable;

public class ChoiseItem implements Serializable{
	private int questionId;
	private boolean isNewAnswer;

	public boolean isNewAnswer() {
		return isNewAnswer;
	}

	public void setNewAnswer(boolean isNewAnswer) {
		this.isNewAnswer = isNewAnswer;
	}

	public int getQuestionId() {
		return questionId;
	}

	public void setQuestionId(int questionId) {
		this.questionId = questionId;
	}

	private int orderNo;
	private String name;
	private boolean isDefalut;
	private boolean recordDeleted;
	private String tag;

	public int getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(int orderNo) {
		this.orderNo = orderNo;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public boolean isDefalut() {
		return isDefalut;
	}

	public void setDefalut(boolean isDefalut) {
		this.isDefalut = isDefalut;
	}

	public boolean isRecordDeleted() {
		return recordDeleted;
	}

	public void setRecordDeleted(boolean recordDeleted) {
		this.recordDeleted = recordDeleted;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	private long id;

	public boolean isChecked() {
		return checked;
	}

	public void setChecked(boolean checked) {
		this.checked = checked;
	}

	boolean checked;
}
